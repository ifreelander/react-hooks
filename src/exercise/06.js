import React from 'react'
import {ErrorBoundary} from 'react-error-boundary'
// üê® you'll want the following additional things from '../pokemon':
// fetchPokemon: the function we call to get the pokemon info
// PokemonInfoFallback: the thing we show while we're loading the pokemon info
// PokemonDataView: the stuff we use to display the pokemon info
import {
  fetchPokemon,
  PokemonForm,
  PokemonInfoFallback,
  PokemonDataView,
} from '../pokemon'

function PokemonInfo({pokemonName}) {
  //const [pokemon, setPokemon] = React.useState(null)
  //const [error, setError] = React.useState(null)
  //const [status, setStatus] = React.useState('idle')

  const [state, setState] = React.useState({
    status: 'idle',
    pokemon: null,
    error: null,
  })

  const {status, pokemon, error} = state

  React.useEffect(() => {
    // remember this useEffect runs on every single render where the pokemonName changes including the initial mount of out component
    // when our component is initialy mounted there is no pokemonName so lets exit early
    if (!pokemonName) {
      return
    }
    setState({status: 'pending'})
    //setStatus('pendig')
    fetchPokemon(pokemonName).then(
      pokemon => {
        // eslint-disable-next-line no-unused-expressions
        //setPokemon(pokemon)
        //setStatus('resolved')
        setState({pokemon, status: 'resolved'})
      },
      error => {
        // eslint-disable-next-line no-unused-expressions
        //setError(error)
        //setStatus('rejected')
        setState({error, status: 'rejected'})
      },
    )
  }, [pokemonName])

  if (status === 'idle') {
    return 'Submit a pokemon'
  } else if (status === 'pending') {
    return <PokemonInfoFallback name={pokemonName} />
  } else if (status === 'rejected') {
    return (
      <div role="alert">
        There was an error:{' '}
        <pre style={{whiteSpace: 'normal'}}>{error.message}</pre>
      </div>
    )
  } else if (status === 'resolved') {
    return <PokemonDataView pokemon={pokemon} />
  }

  // üê® Have state for the pokemon (null)
  // üê® use React.useEffect where the callback should be called whenever the
  // pokemon name changes.
  // üí∞ DON'T FORGET THE DEPENDENCIES ARRAY!
  // üí∞ if the pokemonName is falsy (an empty string) then don't bother making the request (exit early).
  // üê® before calling `fetchPokemon`, make sure to update the loading state
  // üí∞ Use the `fetchPokemon` function to fetch a pokemon by its name:
  //   fetchPokemon('Pikachu').then(
  //     pokemonData => { /* update all the state here */},
  //   )
  // üê® return the following things based on the `pokemon` state and `pokemonName` prop:
  //   1. no pokemon name: 'Submit a pokemon'
  //   2. pokemon name but no pokemon: <PokemonInfoFallback name={pokemonName} />
  //   3. pokemon: <PokemonDataView pokemon={pokemon} />

  // eslint-disable-next-line no-unreachable
  return 'TODO'
}

function App() {
  const [pokemonName, setPokemonName] = React.useState('')

  function handleSubmit(newPokemonName) {
    setPokemonName(newPokemonName)
  }

  return (
    <div className="pokemon-info-app">
      <PokemonForm pokemonName={pokemonName} onSubmit={handleSubmit} />
      <hr />
      <div className="pokemon-info">
        <PokemonInfo pokemonName={pokemonName} />
      </div>
    </div>
  )
}

export default App
